package lv.akurss.lesson2.lecture;

public class ArithmeticOperators {
	public static void main(String[] args) {
		int a = 5;
		int b = 2;
		int c = 3;
		int d = 3;

		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
		System.out.println("================");
		System.out.println(c++);
		System.out.println(c);
		System.out.println(++c);
		System.out.println("================");
		System.out.println(d--);
		System.out.println(d);
		System.out.println(--d);
		
	}
}
